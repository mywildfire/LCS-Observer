    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jList4.getSelectedIndex() >= 0 && jList6.getSelectedIndex() >= 0){
            List<League> leagues = LeagueController.getInstance().getLeagues();
            List<UserTeam> teams = TeamController.getInstance().getAvailableTeams();
            UserTeam theTeam;
            
            //Add team to league
            for(UserTeam team: teams){
                if(team.getName().equals(jList6.getSelectedValue())){
                    theTeam = team;
                    for(League l: leagues){
                        if(l.getName().equals(jList4.getSelectedValue())){
                            if(LeagueController.getInstance().assignTeam(l, team)){
                                //Update assigned teams
                                assignedTeams.addElement(theTeam.getName());
                                
                            }
                            else{
                                jLabel20.setText("League is full");
                            }
                            break;
                        }
                    }
                    break;
                }
            }
            
            //update available team list
            availableTeamListModel.clear();
            for(UserTeam team: TeamController.getInstance().getAvailableTeams()){
                availableTeamListModel.addElement(team.getName());
            }
            
            
        }
        else{
            jLabel20.setText("Select a league and team");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

