    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(!jTextField6.getText().equals("")){
            String teamname = jTextField6.getText();
            if(mainPlayers.size() != 6 || mainPlayers.size() != 6){
                jLabel29.setText("Not enough players");
                return;
            }
            if(!TeamController.getInstance().createTeam(teamname, mainPlayers.toArray(new LCSPlayer[mainPlayers.size()]), (LCSPlayer[])backupPlayers.toArray(new LCSPlayer[mainPlayers.size()]))){
                jLabel29.setText("Duplicate Team name");
            }
            else{
                jLabel29.setText("Team Created");
                jTextField6.setText("");
                jLabel27.setText("");
                jLabel28.setText("");
                
                jLabel22.setText("Team Name: " + teamname);
                
                mainPlayerListModel.clear();
                backupPlayerListModel.clear();
                
                for(LCSPlayer player: mainPlayers){
                    mainPlayerListModel.addElement(player.getName());
                }
                for(LCSPlayer player: backupPlayers){
                    backupPlayerListModel.addElement(player.getName());
                }
                
                jList7.setModel(mainPlayerListModel);
                jList8.setModel(backupPlayerListModel);
                availableTeamListModel.addElement(teamname);
                mainPlayers.clear();
                backupPlayers.clear();
            }
        }
        else{
            jLabel29.setText("Enter a Team name");
        }
    }//GEN-LAST:event_jButton5ActionPerformed


