    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jList9.getSelectedIndex() >= 0){
            List<LCSPlayer> players = DatabaseManager.getInstance().getLCSPlayers();
            String playerName = jList9.getSelectedValue();
            boolean duplicate = false;
            LCSPlayer target = null;
            
            for(LCSPlayer player: players){
                if(player.getName().equals(playerName)){
                    target = player;
                    break; 
                }
            }
            
            for(LCSPlayer play: mainPlayers){
                if(target.getName().equals(play.getName()))duplicate = true;
            }
            for(LCSPlayer play: backupPlayers){
                if(target.getName().equals(play.getName())) duplicate = true;
            }

            
            String team = "<html>Main:<br>";
            
            if(!duplicate && mainPlayers.size() < 6){
                mainPlayers.add(target);
            }
            else{
                jLabel29.setText("Duplicate/Too many");
                return;
            }
            
            for(LCSPlayer player: mainPlayers){
                team += player.getName() + "<br>";
            }
            team += "<html>";
            
            jLabel27.setText(team);
            jLabel29.setText("");
        }
        else{
            jLabel27.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

